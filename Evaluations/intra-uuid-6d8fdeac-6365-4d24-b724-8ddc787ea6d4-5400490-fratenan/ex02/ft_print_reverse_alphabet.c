#include <unistd.h>

void	ft_putchar(char c)
{
	write (STDOUT_FILENO, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	letter;

	letter = 'z';
	while (letter >= 'a')
	{
		ft_putchar(letter);
		letter--;
	}
}

int main(void) 
{
  ft_print_alphabet();
  return 0;
}