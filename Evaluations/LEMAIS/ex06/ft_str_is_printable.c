int	ft_str_is_printable(char *str)
{
	int	g;

	g = 0;
	while (str[g] != '\0')
	{
		if (! (str[g] >= 32 && str[g] <= 126))
		{
			return (0);
		}
		g++;
	}
	return (1);
}
