char	*ft_strupcase(char *str)
{
	unsigned int	a;

	a = -1;
	while (++a, str[a] != '\0')
	{
		if ((str[a] >= 'a') && (str[a] <= 'z'))
			str[a] = str[a] - 32;
	}
	return (str);
}
