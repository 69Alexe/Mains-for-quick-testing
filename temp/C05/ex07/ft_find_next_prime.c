int	ft_is_prime(int nb)
{
	int	i;

	i = 1;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	while (++i, i != nb)
	{
		if (nb % i == 0)
		{
			return (0);
		}
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	i;

	i = nb;
	if (i < 2)
		return (2);
	while (!ft_is_prime(i))
		i++;
	return (i);
}
